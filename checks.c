#include "fillit.h"

int		ft_megacheck(char *s)
{
	int	i;
	int	c;
	int	c2;
	int	ctet;

	i = 0;
	c = 4;
	c2 = 19;
	ctet = 0;
	while (s[i] != 0)
	{
		if (c == 0 && s[i] == '\n')
			c = 4;
		if (s[i] == '#')
			ctet++;
		if (c2 == 0 && s[i] != '\n' && s[i + 1] != '\n' && (ctet % 4 != 0))
			return (-1);

		if (s[i] == '\n' || s[i] == '#' || s[i] == '.')
		{
			i++;
			c--;
			c2--;
		}
		else
			return (-1);
	}
	return (1);
}
